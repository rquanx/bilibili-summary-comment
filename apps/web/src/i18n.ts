export function getInitialLocale(): Locale {
  if (typeof window === "undefined") {
    return DEFAULT_LOCALE;
  }

  const stored = window.localStorage.getItem(STORAGE_KEY);
  return stored === "zh-CN" || stored === "en-US" ? stored : DEFAULT_LOCALE;
}

export function persistLocale(locale: Locale) {
  if (typeof window === "undefined") {
    return;
  }

  window.localStorage.setItem(STORAGE_KEY, locale);
}

export function translate(locale: Locale, key: string, vars: TemplateVars = {}): string {
  const message = MESSAGES[locale][key] ?? MESSAGES["en-US"][key];
  if (!message) {
    return key;
  }

  return typeof message === "function" ? message(vars) : interpolate(message, vars);
}

export function translateOptional(locale: Locale, key: string, vars: TemplateVars = {}): string | null {
  const message = MESSAGES[locale][key] ?? MESSAGES["en-US"][key];
  if (!message) {
    return null;
  }

  return typeof message === "function" ? message(vars) : interpolate(message, vars);
}

export function localizeManagedSettingDefinition<T extends ManagedSettingDefinitionLike>(locale: Locale, definition: T): T {
  if (locale !== "zh-CN") {
    return definition;
  }

  const text = ZH_MANAGED_SETTING_TEXT[definition.key];
  return text ? { ...definition, ...text } : definition;
}

export function localizeSchedulerTask<T extends SchedulerTaskLike>(locale: Locale, task: T): T {
  if (locale !== "zh-CN") {
    return task;
  }

  const text = ZH_SCHEDULER_TASK_TEXT[task.key];
  return text ? { ...task, ...text } : task;
}

function interpolate(template: string, vars: TemplateVars): string {
  return template.replace(/\{(\w+)\}/g, (_match, key: string) => String(vars[key] ?? ""));
}
